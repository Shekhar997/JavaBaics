import java.util.HashSet;

public class HashSetMain {
	
	public static void main(String[] args) {
		HashSet<String> hs = new HashSet<>();
		
		hs.add("Shekhar");
		hs.add("Sharma");
		hs.add("is");
		hs.add("Very");
		hs.add("good");
		hs.add("boy");
		hs.add("a");
		
		System.out.println(hs);
	}

}
